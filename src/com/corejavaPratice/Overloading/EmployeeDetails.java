package com.corejavaPratice.Overloading;

import java.util.Date;

public class EmployeeDetails {
	
	int empId;
	String empName;
	double empSalary;
	Date createDt;
	public EmployeeDetails(int empId, String empName, double empSalary, Date createDt) {
		super();
		this.empId = empId;
		this.empName = empName;
		this.empSalary = empSalary;
		this.createDt = createDt;
	}
	


}
